    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        carro.carro.setId(Integer.parseInt(txtId.getText()));
        carro.localizar();
        int opcao = JOptionPane.showConfirmDialog(null, carro.carro.getNome(), "Deseja Realmente EXCLUIR o FILME", JOptionPane.YES_NO_OPTION);
        if(opcao == JOptionPane.YES_OPTION){
            String msg = carro.atualizar(CarroDAO.EXCLUSAO);
            JOptionPane.showMessageDialog(null, msg);
            limparCampos();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

