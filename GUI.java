    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // Gravar | Verifica se o usuario informou os valores de todos os campos
        if (txtId.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O Codigo não pode ser Validado");
            txtId.requestFocus();
            return;
        }
        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome não pode ser Validado");
            txtNome.requestFocus();
            return;
        }
        if (txtAno.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O Ano não pode ser Validado");
            txtAno.requestFocus();
            return;
        }
        if (txtModelo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O modelo não pode ser Validado");
            txtModelo.requestFocus();
            return;
        }

        if (txtMarca.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A marca não pode ser Validada");
            txtMarca.requestFocus();
            return;
        }

        carro.carro.setId(Integer.parseInt(txtId.getText()));
        carro.carro.setNome(txtNome.getText());
        carro.carro.setAno(txtAno.getText());
        carro.carro.setModelo(txtModelo.getText());
        carro.carro.setMarca(txtMarca.getText());
        String msg = carro.atualizar(CarroDAO.INCLUSAO);
        JOptionPane.showMessageDialog(null, msg);
        limparCampos();
    }//GEN-LAST:event_jButton3ActionPerformed

