    private void atualizarCampos() {
        carro.carro.setId(Integer.parseInt(txtId.getText()));
        if (carro.localizar()) {
            txtId.setText(String.valueOf(carro.carro.getId()));
            txtNome.setText(carro.carro.getNome());
            txtAno.setText(carro.carro.getAno());
            txtModelo.setText(carro.carro.getModelo());
            txtMarca.setText(carro.carro.getMarca());
            setBotoes(true, true, false, true, true, true);
        } else {
            JOptionPane.showMessageDialog(null, "Carro não encontrado!");
            limparCampos();
        }
    }

